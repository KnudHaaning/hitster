// Usage: node scripts/fetch-playlist.mjs <CLIENT_ID> <CLIENT_SECRET> <PLAYLIST_ID>
const [,, CLIENT_ID, CLIENT_SECRET, PLAYLIST_ID] = process.argv;

if (!CLIENT_ID || !CLIENT_SECRET || !PLAYLIST_ID) {
  console.error('Usage: node scripts/fetch-playlist.mjs <CLIENT_ID> <CLIENT_SECRET> <PLAYLIST_ID>');
  process.exit(1);
}

async function getToken() {
  const res = await fetch('https://accounts.spotify.com/api/token', {
    method: 'POST',
    headers: {
      'Content-Type': 'application/x-www-form-urlencoded',
      'Authorization': 'Basic ' + Buffer.from(`${CLIENT_ID}:${CLIENT_SECRET}`).toString('base64'),
    },
    body: 'grant_type=client_credentials',
  });
  const data = await res.json();
  return data.access_token;
}

async function fetchAllTracks(token, playlistId) {
  const tracks = [];
  let url = `https://api.spotify.com/v1/playlists/${playlistId}/tracks?limit=100&fields=next,items(track(uri,name,artists,album(release_date)))`;
  while (url) {
    const res = await fetch(url, { headers: { Authorization: `Bearer ${token}` } });
    const data = await res.json();
    if (data.error) {
      throw new Error(`Spotify API error ${data.error.status}: ${data.error.message}`);
    }
    for (const item of data.items) {
      const t = item.track;
      if (!t || !t.uri) continue;
      tracks.push({
        uri: t.uri,
        title: t.name,
        artist: t.artists.map(a => a.name).join(', '),
        year: t.album.release_date.slice(0, 4),
      });
    }
    url = data.next || null;
  }
  return tracks;
}

const token = await getToken();
const tracks = await fetchAllTracks(token, PLAYLIST_ID);
const output = `// Auto-generated by scripts/fetch-playlist.mjs\n// ${tracks.length} tracks from Hitster playlist\nconst TRACKS = ${JSON.stringify(tracks, null, 2)};\n`;
process.stdout.write(output);
console.error(`✓ Fetched ${tracks.length} tracks`);
